package com.example.myapp;

public class DateTimeInfo {
    private String date;
    private String time;

    // Getters e setters
    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
